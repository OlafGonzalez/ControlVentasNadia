    {
        $cuenta = new Cuentas;

        $cuenta->pagos=$request->input('pago');
        $cuenta->usuario_id=$request->input('cliente');
        $cuenta->fecha_entrega=$request->input('fecha');
        $cuenta->hora_entrega=$request->input('hora');
        $cuenta->save();

        return redirect('Articulo');
    }

